                <h2 class="text-uppercase font-weight-600 mt-0 font-24 line-bottom">History of the Hospital</h2>
                 <article class="post clearfix mb-30 mb-sm-30">
                 
                <div id="accordion2" class="panel-group accordion">
                <div class="panel">
                  <div class="panel-title"> <a class="active" data-parent="#accordion2" data-toggle="collapse" href="#history" aria-expanded="true"> <span class="open-sub"></span>History of the Hospital</a> </div>
                  <div id="history" class="panel-collapse collapse " role="tablist" aria-expanded="true">
                    <div class="panel-content">
                        <p> Babcock University (BU) whose progenitor,   the Adventist College of West Africa (ACWA), was founded by the   Seventh-day Adventist Church in 1959, was later renamed &ldquo;Adventist   Seminary of West Africa (ASWA)&rdquo; in 1975.</p>
   	                                  <p>From the inception of the school there   had been plans to introduce medical services, since Adventist believe   that medical ministry is the right hand of the gospel message. To this   effect Mrs. Sufficool was appointed as the college nurse in 1960, she   treated and care for the minor ailment of students. Severe cases were   transferred to Sagamu General Hospital or the Adventist Hospital   Ile-Ife. She handed over to Mrs. Canthrel in 1963 who worked in the same   office till 1968. </p>
   	                                  <p>In 1964, due to a high demand for medical   services for both the community indigenes and students, Chief J.S.K.   Osibodu one of the community leaders of Ilishan town played a prominent   pioneering role in the establishment of the Adventist Medical Centre in   the community. He wrote several letters to the Adventist leaders both in   Nigeria and overseas advocating for a medical centre in Ilishan town.   Such letters were filed for later considerations. Mrs. V. Awoniyi took   over as the third college nurse from 1968 till 1973. It was difficult   for her to attend to patients from Ilishan community and its environs   due to her work load as the college nurse as well as the Director of   food services/food industries. </p>
   	                                  <p>One early morning a lady from Ilishan   community came knocking at her door, the lady had a very big abscess on   her buttocks. Mrs. V. Awoniyi felt compassionate towards her and   therefore consulted with her husband Pastor J.D. Awoniyi who encouraged   her to become a Good Samaritan. The lady was treated and the following   day she returned with three other people in need of medical care. The   experience was unique in her life as she stated &ldquo;I will never forget   that day&rdquo;. She quickly attended to them and from that day her doors was   opened to all and medical services were extended to the indigenes. She   was compelled to train the young ladies living with her as nurse   assistants because of her other responsibilities. </p>
   	                                  <p>The college administration headed by Dr.   Steward Bekeley contacted the SDA Hospital Ile-Ife on the establishment   of a health centre. The hospital authorities were highly interested and   supportive of the clinic that was established in Pastor and Mrs.   Awoniyi&rsquo;s home. </p>
   	                                  <p>Meanwhile Chief J.S.K. Osibodu wanted   something more than a clinic that was in Mrs. Awoniyi&rsquo;s home, he wanted a   real hospital for his people therefore, he made a passionate appeal to   Dr. Percy Paul the Principal of the college in 1971 as follows: </p>
   	                                  <p><em>&ldquo;â€¦ This is your time, I strongly   advise you to fight a good fight for us, and dip your ten fingers into   this our noble proposed hospital as I told you, one of our sons in Lagos   Chief M.A.K. Sonowo has volunteered his building to be used for this   hospital and our people are vigorously contributing for the progress of   this hospitalâ€¦ On behalf of my Oba, and the entire people of my town,   Ilishan, I am appealing to you to help us to contribute morally,   physically, and in any way you think this our proposed hospital will be   successfulâ€¦ For your information, the Western Government has sent us a   form which we have filled and returned to the Government; an official of   the Medical Department had come to Ilishan to inspect the building and   he has been satisfied with the houseâ€¦ Furthermore, we have taxed   ourselves, about the supply of some hospital equipment, such as beds,   mattresses, and so on. We are asking for your support. If God approves   that college of yours as university, this college cannot do without the   hospital, because I am sure both students and tutors will be increasing   yearly or quarterly, and moreover, you will use the hospital to train   your students who enter the college as medical students. Therefore, the   sooner we arrange for this hospital the betterâ€¦&rdquo; </em></p>
   	                                  <p>The above request was favored by the   college administration, relevant documents were put together and   necessary actions were taken for the government approval of the health   centre. Chief Osibodu and his brothers also secured a building in the   community. In September 1972 the school board voted to have a clinic at   Ilishan and the clinic is to be housed at &ldquo;Owodunni&rsquo;s house&rdquo;.</p>
   	                                  <p>The Adventist College Clinic was   officially opened on October 8, 1972. At the Clinic opening ceremony   J.S.K. Osibodu and V. Awoniyi were honored with chieftaincy tittles by   the Oba Onasoga due to their humanitarian activities in bringing the   clinic to the community. Mrs. V. Awoniyi handed over the clinic   management to Mrs. Christiana Onwere a registered nurse in 1973; she   also worked successfully for a year when Mrs. Diety a white lady took   over from her. Meanwhile, Dr. Mrs. Clemonds a medical missionary   (expatriate) came with her husband to join the college workforce. She   took over the leadership of the clinic and dedicated herself to the   improvement and performances of the college clinic. As a physician she   took care of the administration of the health centre for a long time,   the number of clients increased greatly and other workers were employed   as auxiliaries. As the clinic was stabilizing, the landlord of Owodunni   house died and conflicts broke out among the children, this affected the   work at the clinic. A decision was then taken by the community and the   college administration to look for another site.</p>
   	                                  <p>In 1979 the college was moved from   Owodunni&rsquo;s house to Sofola&rsquo;s house on Irolu road. Though the place was   smaller, yet the clinic functioned under Dr. Mrs. Clemonds, while   waiting and hoping for a better place. In the meantime, chief Osibodu   appealed again to the college Administration that the clinic be   developed to a hospital. In 1982 the combined efforts of late Chief   Adetola (The Balogun), Mr. J.O. Famodu, and the late Oba Onasoga made a   strong appeal requesting the Adventist Mission to build a hospital at   Ilishan. The request was granted and on December 26 1984, the foundation   stone of the present BUTH was laid. The main building was completed and   the clinic at Sofola&rsquo;s house was moved to the new building at the   Seminary campus on January 1, 1986.</p>
